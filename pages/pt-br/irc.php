<h1>Converse conosco no IRC</h1>

<p>Muitos dos desenvolvedores e usuários do Freenet podem ser encontrados frequentemente no canal <a href="https://en.wikipedia.org/wiki/IRC">IRC</a>,
#freenet no chat.freenode.net. Quase todos no canal são voluntários, e podem estar ocupados com outras coisas, então pode ser que você não receba uma resposta instantânea: <b>Faça sua pergunta/diga qual é p problema, então espere alguns minutos, e alguém pode ajudá-lo</b>.</p>

<a href="https://webchat.freenode.net/?randomnick=1&channels=freenet">Converse conosco</a>

<p>Se você não obter uma resposta ainda nos primeiros minutos, por favor, mantenha a janela do bate-papo aberta. Nós sempre lemos nosso backlog, e se você ficar, normalmente receberá uma resposta em no máximo <b>4 horas</b> (quando alguém com conhecimentos relevantes tomar conhecimento).<p>
