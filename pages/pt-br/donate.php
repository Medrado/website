      <h4>Por que o Projeto Freenet precisa de doações?</h4>
      <p>
	Muito embora incontáveis pessoas tenham doado seu tempo e suas habilidades ao projeto desde que ele se iniciou, o Projeto Freenet confia em suas doações tanto para apoiar estes desenvolvedores	trabalhando em tempo integral neste crescente e complexo projeto, quanto para cobrir despesas de hardware (este servidor) e custos administrativos (ex. registro de domínios). Também gostaríamos de começar um fundo de defesa legal para casos em que se necessite. </p><p> Todos os envolvidos estão muito interessados no futuro do projeto Freenet e você pode ter certeza de que suas doações continuarão a ser usadas com sabedoria. Toda ajuda é bem vinda.</p>
      
