<p><a class='wiki'  href='http://www.freenet.org.nz/python/pyFreenet/'><b>pyFreenet</b></a><br />
<br />
pyFreenet é uma API e um conjunto de ferramentas para acessar Freenet apartir da linguagem de programação python. Atualmente as ferramentas são apenas de linha de comando.
</p>

<p><a class='wiki'  href='https://wiki.freenetproject.org/FreenetJsite'><b>jSite</b></a><br />
<br />
jSite é uma aplicação gráfica que você pode usar para criar, inserir e administrar seus próprios sites Freenet.
</p>

<p><a class='wiki'  href="http://nostromo.joeh.org/content/fn_curses/..html"><b>fn_curses</b></a><br />
