      <a name="free_flow_info"><h3> 3. A importância do livre fluxo de informação  </h3></a>
      
      <p>
	Liberdade de expressão, na maioria das culturas ocidentais, é geralmente considerada como um dos mais importantes direitos que qualquer indivíduo pode ter. Por que a liberdade de compartilhar ideias e opiniões é tão importante? Há várias maneiras de responder esta questão.
      </p>
      
      <h4> 3.1 Comunicação é o que nos faz humanos </h4>

      <p>
	Uma das diferenças mais óvias entre o ser humano e o restante do reino animal é nossa habilidade para comunicar conceitos abstratos e sofisticados. Enquanto descobrimos constantemente que a capacidade de comunicação dos animais é mais sofisticada do que anteriormente se supunha, é improvável que qualquer outro animal se aproxime do nosso nível de capacidade nesta área.
      </p>
      
      <h4> 3.2 Conhecimento é bom </h4>
      
      <p>
	A maioria das pessoas, dada a opção de saber alguma coisa ou não saber nada, escolherá ter mais informações em vez de menos. Guerras Têm sido ganhas e perdidas por quem estava melhor informado. Isso porque estando melhor informados nos permite tomar melhores decisões,e, geralmente melhorar  nossa capacidade de sobreviver e de ser bem sucedido.
      </p>
      
      <h4> 3.3 Democracia presupõe uma população bem informada </h4>
	
      <p>
	Muita gente hoje vive sob governos democráticos, e aqueles que não, provavelmente queiram. Democracia é uma resposta para a questão de como criar líderes, ao mesmo tempo que os impede de abusar deste poder. Ela consegue isso dando à população o poder de regular seu governo através do voto, mas, a capacidade de votar não siginifica necessariamente que você viva em um país democrático. Para uma população regular seu governo de maneira eficaz, deve saber o que ele está fazendo, deve estar bem informada. Trata-se de um laço de reação, mas este laço pode ser quebrado se o governo tiver o poder de controlar as informações que a população tem acesso. </p>

      <a name="censor_freedom"><h3> 4. Censura e liberdade </h3></a>

      <p>
	Todo mundo valoriza sua liberdade, na verdade, muitos a consideram tão importante que morrerão por ela. As pessoas gostam de pensar que são livres para formar e manter as opiniões que quiserem, particularmente nos países ocidentais. Imagine agora que alguém tenha a capacidade de controlar a informação que você acessa. Isso seria como dar-lhe a capacidade de manipular suas opiniões, ocultar fatos, apresentando-os como sendo mentiras e censurando qualquer coisa que contradiga essas mentiras. Isto não é uma ficção Orwelliana, é uma prática padrão para a maioria dos governos ocidentais, mentir para a população, tanto é assim, que agora as pessoas têm isso como certo, apesar do fato de que isso contraria os próprios princípios democráticos que justificam a existência do governo. 
      </p>

      <a name="solution"><h3> 5. A solução </h3></a>

      <p>
	A única maneira de garantir que uma democracia vai manter sua eficácia é assegurar que o governo não possa controlar a capacidade da população de compartilhar informação, de comunicar-se. Absolutamente tudo o que vemos e ouvimos é filtrado, não somos verdadeiramente livres. O objetivo da Freenet é permitir que duas ou mais pessoas que queiram compartilhar informação, façam isso.
      </p>

      <a name="is_censorship_necessary"><h3> 6. Censura não é necessária as vezes? </h3></a>
      <p>
	Obviamente nenhum problema é preto e branco, e há muitos que consideram  que a censura seja uma coisa boa em algumas circunstâncias. por exemplo, em alguns paíse europeus propagar informações consideradas racistas é ilegal. Os governos procuram evitar que as pessoas defendam ideias que são consideradas prejuduiciais para a sociedade. Existem no entanto, duas respostas para isso.
	A primeira é que não se pode permitir que aqueles que estão no poder imponham uma "boa" censura, sem que lhes permitam impor uma censura "má". Para impor qualquer forma de censura um governo deve ter a capacidade de monitorar e assim acaba por restringir a comunicação. Já existem críticas de que a censura anti-racismo em muitos países europeus está dificultando a análise histórica legítima de eventos, tais como a segunda guerra mundial. 
      </p>

      <p>
	O segundo argumento é que esta "boa" censura é contra-produtiva, mesmo quando ela não vaza para outras áreas. Por exemplo, geralmente é mais eficaz quando se tenta persuadir alguém de algo apresentando os argumentos contra ela, e então responder a estes argumentos. Infelizmente, impedindo as pessoas de estarem cientes dos argumentos sofisticados, muitas vezes utilizados por racistas, torna-os vulneráveis a estes argumentos, quando eventualmente, os encontram. 
      </p>

      <p> 
	É claro que o primeiro argumento é mais forte, e ainda assim, mantém-se como verdadeiro, mesmo se você não aceitar o segundo. Basicamente,  ou você quer ter a censura, ou você não quer. Não há meio termo.
      </p>

      <a name="why_anonymity"><h3> 7. Mas, por que o anonimato é necessário? </h3></a>

      <p>
	Você não pode ter liberdade de expressão, sem a opção de permanecer anônimo. A maioria da censura é retrospectiva, geralmente é mais fácil reduzir a liberdade de expressão punindo aqueles que a exercem, depois, em vez de impedí-los de fazer primeiro. A única maneira de prevenir isso é permanecer anônimo. É um equívoco comum não se confiar em informações anônimas. Isto não é necessariamente uma verdade, usando assinaturas digitais seguras as pessoas podem criar pseudônimos anonônimos, que com o tempo, as pessoas aprendem a confiar.
	Freenet incorpora um mecanismo chamado de "subspaces" para facilitar isso. 
      </p>
      
      <a name="copyright"><h3> 8. E o direito autoral? </h3></a>

      <p>
	Claro que muito da publicidade do Freenet está centrado em torno da questão dos direitos autorais, então falarei brevemente sobre ele. O principal problema com o direito do autor (copyright) é que a aplicação dele exige monitoramento das comunicações, e não se pode ter garantia de livre expressão enquanto se é monitorado em tudo o que se diz. Isso é importante, a maioria das pessoas não consegue ver ou abordar este ponto quando debate sobre a questão dos direitos autorais, então, deixe-me esclarecer que:
	<center>Não é possível garantir a liberdade de expressão e fazer cumprir a lei de direitos autorais</center> 
      </p>
      <p>
	É por esta razão que Freenet, um sistema concebido para proteger a liberdade de expressão, deve evitar a execução de direitos autorais.
      </p>

      <a name="reward"><h3> 9. Mas, como os artistas serão recompensados pelos seus trabalhos? </h3></a>
      
      <p>
	Primeiramente, ainda que os direitos autorais fossem a única maneira de recompensar um artista por seu trabalho, eu afirmaria que a liberdade é mais importante do que ter artistas profissionais (aqueles que afirmam que não teremos mais arte não entendem de criatividade: as pessoas sempre criarão, é uma compulsão, a única questão é saber se eles podem fazê-la para viver).
      </p>
      <p>
	Em segundo lugar, poderia ser questionado se o direito autoral (copyright) é eficaz, neste momento. A indústria da música é uma das mais veementes vozes contra o avanço da tecnologia da comunicação, ainda assim, de acordo com muitos artistas que deveria ser recompensados por direitos autorais, ela deixa de fazê-lo. Em vez disso, permitiu que intermediários ganhassem controle sobre os mecanismos de distribuição, em detrimento de  artistas e público.
      </p>
      
      <a name="alternative"><h3> 10. Alternativas ao direito autoral (Copyright) </h3></a>
      
      <p>
	Felizmente, isso não vai acontecer. Há muitas formas de recompensar os artistas. O mais simples é o pagamento voluntário. Esta é uma extensão do clientelismo que era frequentemente usado para premiar artistas antes dos direitos autorais, onde uma pessoa rica financiaria um artista para que ele pudesse criar em tempo integral. A Internet permite uma interessante extensão desta ideia, onde em vez de apenas um rico patrono, você poderia ter centenas de milhares, contribuindo com pequenas quantias em dinheiro através da internet.
      </p>
      <p> 
	Nós realmente praticamos o que pregamos também no que se refere a este assunto, no dia 15 de março de 2001, o Projeto Freenet Project iniciou o pedido de doações, e em uma semana já tínhamos coletado mais de 1.000 dólares americanos. 
      </p>
      
